function collectPayload() {
  return {
    query: document.getElementById("query-input").value.trim(),
    language: document.getElementById("language-select").value,
    patient: {
      age: document.getElementById("age-input").value || null,
      gender: document.getElementById("gender-input").value || null,
      pregnancy_status: document.getElementById("pregnancy-input").value || null,
      allergies: splitCommaSeparated(document.getElementById("allergies-input").value),
      existing_conditions: splitCommaSeparated(document.getElementById("conditions-input").value),
    },
  };
}

function resetForm() {
  document.getElementById("query-form").reset();
  document.getElementById("results-panel").classList.add("hidden");
  document.getElementById("urgent-card").classList.add("hidden");
}

async function submitQuery(event) {
  event.preventDefault();

  const payload = collectPayload();
  const copy = getCopy(payload.language);
  if (!payload.query) {
    return;
  }

  const submitButton = document.getElementById("submit-button");
  const originalText = submitButton.textContent;
  submitButton.disabled = true;
  submitButton.textContent = copy.loading;

  try {
    const response = await fetch("/query", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify(payload),
    });

    if (!response.ok) {
      throw new Error("Request failed");
    }

    const data = await response.json();
    renderResponse(data, payload.query);
  } catch (error) {
    window.alert(copy.errorMessage);
    console.error(error);
  } finally {
    submitButton.disabled = false;
    submitButton.textContent = originalText;
  }
}

function renderResponse(data, originalQuery) {
  const language = String(document.getElementById("language-select").value || "en").toLowerCase();
  document.getElementById("language-select").value = language;
  setStaticCopy(language);
  const copy = getCopy(language);

  document.getElementById("results-panel").classList.remove("hidden");
  document.getElementById("summary-query-value").textContent = originalQuery;

  const topConfidence = Array.isArray(data.conditions) && data.conditions.length > 0
    ? Number(data.conditions[0].confidence) || 0
    : 0;
  const confidenceLevel = confidenceLevelForScore(topConfidence);
  const confidenceBadge = document.getElementById("confidence-badge");
  confidenceBadge.textContent = copy.confidence[confidenceLevel] || confidenceLevel;
  confidenceBadge.style.background = confidenceClass(confidenceLevel);
  confidenceBadge.style.color = confidenceTextColor(confidenceLevel);

  const urgencyState = getUrgencyState(data);
  const urgentCard = document.getElementById("urgent-card");
  if (urgencyState.urgent && urgencyState.urgentMessage) {
    urgentCard.textContent = urgencyState.urgentMessage;
    urgentCard.classList.remove("hidden");
  } else {
    urgentCard.classList.add("hidden");
    urgentCard.textContent = "";
  }

  renderChipList("symptom-list", data.symptoms, copy.symptomsEmpty);
  renderConditions(data.conditions, language, urgencyState.urgent);
  renderBulletList(
    "treatment-list",
    urgencyState.urgent ? [] : data.treatments,
    urgencyState.urgent ? copy.urgentSafeOnly : copy.treatmentsEmpty
  );
  renderBulletList(
    "homecare-list",
    urgencyState.urgent ? [] : data.home_care,
    urgencyState.urgent ? copy.urgentSafeOnly : copy.homecareEmpty
  );
  renderMedicines(data.otc_medicines, "otc-list", copy.otcEmpty, language, urgencyState.urgent);
  renderMedicines(
    data.prescription_medicines,
    "prescription-list",
    copy.prescriptionEmpty,
    language,
    urgencyState.urgent
  );
  renderBulletList("warnings-list", urgencyState.displayWarnings, copy.warningsEmpty);
  renderInteractions(data.interaction_warnings, language, urgencyState.urgent);
  document.getElementById("disclaimer-text").textContent = copy.disclaimerText || getCopy("en").disclaimerText || "";

  document.getElementById("results-panel").scrollIntoView({ behavior: "smooth", block: "start" });
}


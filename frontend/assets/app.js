function renderChipList(containerId, items, emptyText) {
  const container = document.getElementById(containerId);
  if (!Array.isArray(items) || items.length === 0) {
    container.innerHTML = `<div class="empty-state">${escapeHtml(emptyText)}</div>`;
    return;
  }
  container.innerHTML = items
    .map(item => `<span class="chip">${escapeHtml(item)}</span>`)
    .join("");
}

function renderBulletList(containerId, items, emptyText) {
  const container = document.getElementById(containerId);
  if (!Array.isArray(items) || items.length === 0) {
    container.innerHTML = `<li class="empty-state">${escapeHtml(emptyText)}</li>`;
    return;
  }
  container.innerHTML = items
    .map(item => `<li>${escapeHtml(item)}</li>`)
    .join("");
}

function renderConditions(conditions, language, isUrgent) {
  const copy = getCopy(language);
  const container = document.getElementById("conditions-list");
  if (isUrgent) {
    container.innerHTML = `<div class="empty-state">${escapeHtml(copy.urgentSafeOnly)}</div>`;
    return;
  }
  if (!Array.isArray(conditions) || conditions.length === 0) {
    container.innerHTML = `<div class="empty-state">${escapeHtml(copy.conditionsEmpty)}</div>`;
    return;
  }

  container.innerHTML = conditions
    .map(condition => {
      const level = confidenceLevelForScore(Number(condition.confidence) || 0);
      return `
        <article class="condition-card">
          <div class="condition-head">
            <h4 class="condition-title">${escapeHtml(condition.condition)}</h4>
            <span class="score-pill ${level}">${Math.round((Number(condition.confidence) || 0) * 100)}%</span>
          </div>
          <p class="condition-reason">${escapeHtml(condition.reason)}</p>
        </article>
      `;
    })
    .join("");
}

function renderMedicines(medicines, containerId, emptyText, language, isUrgent) {
  const copy = getCopy(language);
  const container = document.getElementById(containerId);
  if (isUrgent) {
    container.innerHTML = `<div class="empty-state">${escapeHtml(copy.urgentSafeOnly)}</div>`;
    return;
  }
  const entries = Array.isArray(medicines) ? medicines : [];
  if (entries.length === 0) {
    container.innerHTML = `<div class="empty-state">${escapeHtml(emptyText)}</div>`;
    return;
  }

  container.innerHTML = entries
    .map(medicine => {
      const brandList = Array.isArray(medicine.brands) && medicine.brands.length > 0
        ? `
          <div class="medicine-meta">
            <span class="medicine-meta-label">Brands</span>
            ${medicine.brands.map(item => `<span class="inline-tag">${escapeHtml(item)}</span>`).join("")}
          </div>
        `
        : "";
      const related = Array.isArray(medicine.related_conditions) && medicine.related_conditions.length > 0
        ? `
          <div class="medicine-meta">
            <span class="medicine-meta-label">${escapeHtml(copy.relatedConditions)}</span>
            ${medicine.related_conditions.map(item => `<span class="inline-tag">${escapeHtml(item)}</span>`).join("")}
          </div>
        `
        : "";
      const usesText = medicine.uses || medicine.purpose || "";
      const warningsList = Array.isArray(medicine.safety_notes) && medicine.safety_notes.length > 0
        ? medicine.safety_notes
        : (medicine.warnings ? [medicine.warnings] : []);
      const safetyNotes = warningsList.length > 0
        ? `
          <h5>${escapeHtml(copy.safetyNotes)}</h5>
          <ul class="detail-list">
            ${warningsList.map(note => `<li>${escapeHtml(note)}</li>`).join("")}
          </ul>
        `
        : "";
      const patientWarning = medicine.patient_specific_warning
        ? `<div class="medicine-warning"><strong>${escapeHtml(copy.patientWarning)}:</strong> ${escapeHtml(medicine.patient_specific_warning)}</div>`
        : "";
      return `
        <article class="medicine-card">
          <div class="medicine-head">
            <h4 class="medicine-title">${escapeHtml(medicine.name)}</h4>
            <span class="medicine-type">${escapeHtml(medicine.type || "Medicine")}</span>
          </div>
          <p class="medicine-purpose">${escapeHtml(usesText)}</p>
          ${brandList}
          ${related}
          ${safetyNotes}
          ${patientWarning}
        </article>
      `;
    })
    .join("");
}


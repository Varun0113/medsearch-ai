function getCopy(language) {
  return uiCopy[language] || uiCopy.en;
}

function splitCommaSeparated(value) {
  return String(value || "")
    .split(",")
    .map(item => item.trim())
    .filter(Boolean);
}

function escapeHtml(value) {
  return String(value || "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#39;");
}

function confidenceClass(level) {
  if (level === "high") return "rgba(15, 109, 84, 0.14)";
  if (level === "medium") return "rgba(183, 101, 44, 0.14)";
  return "rgba(155, 44, 44, 0.14)";
}

function confidenceTextColor(level) {
  if (level === "high") return "#0a523f";
  if (level === "medium") return "#8a4f22";
  return "#9b2c2c";
}

function confidenceLevelForScore(score) {
  if (score >= 0.75) return "high";
  if (score >= 0.55) return "medium";
  return "low";
}

function getUrgencyState(data) {
  const warnings = Array.isArray(data.warnings) ? data.warnings : [];
  const urgent = String(warnings[0] || "") === "URGENT MEDICAL ATTENTION REQUIRED";
  return {
    urgent,
    urgentMessage: urgent ? warnings.slice(0, 2).filter(Boolean).join("\n\n") : "",
    displayWarnings: urgent ? warnings.slice(2) : warnings,
  };
}

